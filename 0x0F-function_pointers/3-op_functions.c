#include "3-calc.h"

/**
 * op_add - add operation
 * @a: left operand
 * @b: right operand
 * Return: integer answer
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - subtract operation
 * @a: left operand
 * @b: right operand
 * Return: integer answer
 */
int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_mul - multiply operation
 * @a: left operand
 * @b: right operand
 * Return: integer answer
 */
int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * op_div - divide operation
 * @a: left operand
 * @b: right operand
 * Return: integer answer
 */
int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
 * op_mod - mod operation
 * @a: left operand
 * @b: right operand
 * Return: integer answer
 */
int op_mod(int a, int b)
{
	return ((a) % (b));
}
